public class Array{
    public static void main(String[] args){
        int[] numbers = {1,2,3,4,5};
        System.out.println(numbers[0]);

        String[] words = {"he", "ll", "o"};
        System.out.println(words[0]);

        for(int i=0; i<words.length; i++){
            System.out.println(words[i]);
        }
    }
}